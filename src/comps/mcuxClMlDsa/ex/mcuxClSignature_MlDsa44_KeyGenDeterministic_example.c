static mcuxClRandom_Status_t MlDSA_Seed_RNG_Patch_function(
  mcuxClSession_Handle_t session,
  mcuxClRandom_Context_t pCustomCtx,
  mcuxCl_Buffer_t pOut,
  uint32_t outLength
)
{
    (void)session;
    (void)pCustomCtx;

    MCUX_CSSL_FP_FUNCTION_CALL_BEGIN(be_status, be_token, mcuxClBuffer_export(pOut, 0u, seed_KAT, outLength));
    if((MCUX_CSSL_FP_FUNCTION_CALLED(mcuxClBuffer_export) != be_token) || (MCUXCLBUFFER_STATUS_OK != be_status))
    {
        return MCUXCLRANDOM_STATUS_FAULT_ATTACK;
    }
    MCUX_CSSL_FP_FUNCTION_CALL_END();

    return MCUXCLRANDOM_STATUS_OK;
}

#define MAX_CPUWA_SIZE \
    MCUXCLCORE_MAX( \
      MCUXCLRANDOM_NCINIT_WACPU_SIZE, \
      MCUXCLCORE_MAX(MCUXCLRANDOMMODES_CREATEPATCHMODE_WACPU_SIZE, MCUXCLMLDSA_MLDSA44_KEYGEN_SIZEOF_WA_CPU) \
    )

MCUXCLEXAMPLE_FUNCTION(mcuxClSignature_MlDsa44_KeyGenDeterministic_example)
{
    /******************************************/
    /* Set up the environment                 */
    /******************************************/

    /* Setup one session to be used by all functions called */
    mcuxClSession_Descriptor_t sessionDesc;
    mcuxClSession_Handle_t session = &sessionDesc;

    /* Allocate and initialize session */
    /* NOTE: Workarea size is to be specified in bytes (no PKC workarea is used for key generation) */
    MCUXCLEXAMPLE_ALLOCATE_AND_INITIALIZE_SESSION(session, MAX_CPUWA_SIZE, 0U);

    /******************************************/
    /* Initialize private and public key      */
    /******************************************/
    /* Allocate space for and initialize private key handle for a ML-DSA-44 private key */
    uint32_t privKeyDesc[MCUXCLKEY_DESCRIPTOR_SIZE_IN_WORDS];
    mcuxClKey_Handle_t privKey = (mcuxClKey_Handle_t)&privKeyDesc;
    uint32_t pPrivateKeyData[MCUXCLCORE_NUM_OF_CPUWORDS_CEIL(MCUXCLMLDSA_MLDSA44_SK_LEN)];

    const mcuxClKey_Status_t ki_priv_status = mcuxClKey_init(
      /* mcuxClSession_Handle_t session: */ session,
      /* mcuxClKey_Handle_t key:         */ privKey,
      /* mcuxClKey_Type_t type:          */ mcuxClKey_Type_MlDsa44_Private,
      /* uint8_t* pKeyData:             */ (uint8_t*)pPrivateKeyData,
      /* uint32_t keyDataLength:        */ sizeof(pPrivateKeyData)
    );

    if(MCUXCLKEY_STATUS_OK != ki_priv_status)
    {
        return MCUXCLEXAMPLE_STATUS_ERROR;
    }

    /* Allocate space for and initialize public key handle for a ML-DSA-44 public key */
    uint32_t pubKeyDesc[MCUXCLCORE_NUM_OF_CPUWORDS_CEIL(MCUXCLKEY_DESCRIPTOR_SIZE)];
    mcuxClKey_Handle_t pubKey = (mcuxClKey_Handle_t)&pubKeyDesc;
    uint32_t pPublicKeyData[MCUXCLCORE_NUM_OF_CPUWORDS_CEIL(MCUXCLMLDSA_MLDSA44_PK_LEN)];

    const mcuxClKey_Status_t ki_pub_status = mcuxClKey_init(
      /* mcuxClSession_Handle_t session: */ session,
      /* mcuxClKey_Handle_t key:         */ pubKey,
      /* mcuxClKey_Type_t type:          */ mcuxClKey_Type_MlDsa44_Public,
      /* uint8_t * pKeyData:            */ (uint8_t*)pPublicKeyData,
      /* uint32_t keyDataLength:        */ sizeof(pPublicKeyData)
    );

    if(MCUXCLKEY_STATUS_OK != ki_pub_status)
    {
        return MCUXCLEXAMPLE_STATUS_ERROR;
    }

    /******************************************/
    /* Initialize the patched RNG             */
    /******************************************/

    /* Fill mode descriptor with the relevant data */
    uint32_t
      customModeDescBytes[(MCUXCLRANDOMMODES_PATCHMODE_DESCRIPTOR_SIZE + (sizeof(uint32_t)) - 1U) / sizeof(uint32_t)];
    mcuxClRandom_ModeDescriptor_t* mcuxClRandomModes_MlDsa_Seed = (mcuxClRandom_ModeDescriptor_t*) customModeDescBytes;
    mcuxClRandomModes_CustomGenerateAlgorithm_t seedFunction = (mcuxClRandomModes_CustomGenerateAlgorithm_t) MlDSA_Seed_RNG_Patch_function;

    MCUX_CSSL_FP_FUNCTION_CALL_BEGIN(
      cp_status,
      cp_token,
      mcuxClRandomModes_createPatchMode(
        /* mcuxClRandom_ModeDescriptor_t * patchMode,                           */ mcuxClRandomModes_MlDsa_Seed,
        /* mcuxClRandomModes_CustomGenerateAlgorithm_t customGenerateAlgorithm, */ seedFunction,
        /* mcuxClRandom_Context_t pCustomCtx,                                   */ NULL,
        /* uint32_t securityStrength                                           */ 256U
      )
    );

    if((MCUX_CSSL_FP_FUNCTION_CALLED(mcuxClRandomModes_createPatchMode) != cp_token) ||
       (MCUXCLRANDOM_STATUS_OK != cp_status))
    {
        return MCUXCLEXAMPLE_STATUS_ERROR;
    }
    MCUX_CSSL_FP_FUNCTION_CALL_END();

    uint32_t context[MCUXCLRANDOMMODES_PATCHMODE_CONTEXT_SIZE_IN_WORDS];
    MCUX_CSSL_FP_FUNCTION_CALL_BEGIN(
      ri_status,
      init_token,
      mcuxClRandom_init(session, (mcuxClRandom_Context_t)context, mcuxClRandomModes_MlDsa_Seed)
    );

    if((MCUX_CSSL_FP_FUNCTION_CALLED(mcuxClRandom_init) != init_token) || (MCUXCLRANDOM_STATUS_OK != ri_status))
    {
        return MCUXCLEXAMPLE_STATUS_ERROR;
    }
    MCUX_CSSL_FP_FUNCTION_CALL_END();

    /******************************************************************************/
    /* Key pair generation                                                        */
    /******************************************************************************/

    const mcuxClKey_Status_t gkp_status = mcuxClKey_generate_keypair(
      /* mcuxClSession_Handle_t pSession:   */ session,
      /* mcuxClKey_Generation_t generation: */ mcuxClKey_keyGeneration_MlDsa44,
      /* mcuxClKey_Handle_t privKey:        */ privKey,
      /* mcuxClKey_Handle_t pubKey:         */ pubKey
    );
    if(MCUXCLKEY_STATUS_OK != gkp_status)
    {
        return MCUXCLEXAMPLE_STATUS_ERROR;
    }

    /**************************************************************************/
    /* Compare generated public key with KAT vector                           */
    /**************************************************************************/
    for(uint32_t i = 0U; i < MCUXCLMLDSA_MLDSA44_PK_LEN; i++)
    {
        if(pk_KAT[i] != ((uint8_t *) pPublicKeyData)[i])
        {
            return MCUXCLEXAMPLE_STATUS_ERROR;
        }
    }

    /**************************************************************************/
    /* Compare generated private key with KAT vector                          */
    /**************************************************************************/
    for(uint32_t i = 0U; i < MCUXCLMLDSA_MLDSA44_SK_LEN; i++)
    {
        if(sk_KAT[i] != ((uint8_t *) pPrivateKeyData)[i] )
        {
            return MCUXCLEXAMPLE_STATUS_ERROR;
        }
    }

    return MCUXCLEXAMPLE_STATUS_OK;
}
